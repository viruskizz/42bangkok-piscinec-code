#include "../headers/util.h"

char	*ft_str_upcase(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
	{
		if (*(str + i) > 96 && *(str + i) < 123)
			*(str + i) = *(str + i) - 32;
		i++;
	}
	return (str);
}
