char	*ft_str_trim(char *str)
{
	int	i;

	i = 0;
	while (*str <= 32)
	{
		i++;
		str++;
	}
	while (str[i] != '\0')
		i++;
	while (str[i] <= 32)
		i--;
	str[i + 1] = '\0';
	return (str);
}
