#include "../headers/util.h"

char	*ft_str_lowcase(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
	{
		if (*(str + i) > 64 && *(str + i) < 91)
			*(str + i) = *(str + i) + 32;
		i++;
	}
	return (str);
}
