#include "../headers/util.h"

char	*ft_str_copy(char *dst, char *src)
{
	int	i;

	i = 0;
	while (*src)
	{
		*(dst + i) = *src;
		src++;
		i++;
	}
	*dst = '\0';
	return (dst);
}
