#include "../headers/util.h"

char	*ft_str_ncopy(char *dst, char *src, int n)
{
	int	i;

	i = 0;
	while (*src && i < n)
	{
		*(dst + i) = *src;
		src++;
		i++;
	}
	*(dst + i) = '\0';
	return (dst);
}
